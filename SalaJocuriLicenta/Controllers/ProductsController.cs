    public class ProductsController : Controller
    {
        private readonly ApplicationDbContext _context;

        public ProductsController(ApplicationDbContext context)
        {
            _context = context;
        }

        // GET: Products
        public async Task<IActionResult> Index(string search, int CategoryId, string sortOrder)
        {
            var product = new List<Product>();
            bool searched = false;
            if (search != null)
            {
                product = _context.Products.Where(p => p.ProductName.Contains(search)).ToList();
                searched = true;
                return View(product);
            }
            ViewData["NameSortParm"] = String.IsNullOrEmpty(sortOrder) ? "name_desc" : "";
            ViewData["DateSortParm"] = sortOrder == "Date" ? "date_desc" : "Date";
            ViewData["PriceSortParm"] = sortOrder == "0" ? "10" : "50";

            var products = from p in _context.Products
                           select p;

            var applicationDbContext = _context.Products.Include(p => p.Category);
            if(CategoryId != 0)
                products = products.Where(c => c.CategoryId == CategoryId);
            return View(await products.ToListAsync());

            switch (sortOrder)
            {
                case "name_desc":
                    products = products.OrderByDescending(p => p.ProductName);
                    break;
                case "10":
                    products = products.OrderBy(p => p.ProductPrice > 10);
                    break;
                default:
                    products = products.OrderBy(p => p.ProductName);
                    break;
            }

        }




